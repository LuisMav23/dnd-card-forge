/**
 * Resolve an icon value: if it's a registered icon ID, return it as-is;
 * if it's a legacy emoji, map it to the new icon ID; otherwise return as-is
 * (IconDisplay will render the raw string as fallback text).
 */
export function resolveIconId(value: string): string {
  if (ICON_REGISTRY[value]) return value;
  return EMOJI_TO_ICON_ID[value] ?? value;
}
